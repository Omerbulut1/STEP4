package STEP4;

public class A1_Do_While {
    public static void main(String[]args){
        int sayi1=0, sayi2=0;

        do{
            sayi1++;
        }
            while(sayi1<0);

        while(sayi2<0){
            sayi2++;
        }
        System.out.println(sayi1);
        System.out.println(sayi2);

    }
}
